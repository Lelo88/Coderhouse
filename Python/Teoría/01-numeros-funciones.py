import math

print(round(1.3)) #retorna el número redondeado al entero mas cercano - 1
print(round(1.7)) #retorna el número redondeado al entero más cercano - 2

print(abs(1.3)) #retorna el número absoluto - 1.3

#podemos importar un modulo para utilizar funciones matematicas

print(math.ceil(1.1)) #retorna el número redondeado al entero más alto - 2
print(math.floor(1.1)) #retorna el número redondeado al entero más bajo - 1
print(math.isnan(1.1)) #retorna True si el número es nan 
print(math.pow(2,3)) #retorna 8
print(math.sqrt(9)) #retorna 3.0
