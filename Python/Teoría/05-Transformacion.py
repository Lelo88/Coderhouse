#En Python, podemos convertir una lista a una tupla haciendo uso de la función tuple() 
# y a su vez, podemos hacer lo mismo, pero a la inversa, es decir, convertir una tupla 
# a lista usando la función list().

numeros = (1,2,3,4)
listaNum= list(numeros)

datos=["hola", "leandro", 7,8,9]
tuplaDatos = tuple(datos)

print(listaNum)
print(tuplaDatos) 
